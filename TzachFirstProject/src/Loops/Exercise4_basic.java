package Loops;

public class Exercise4_basic {

	public static void main(String[] args) {
		
		for (int i = 11; i <= 101; i++) {
			if (i % 2 == 1 ) {
				System.out.println(i);
				
			}
		
		
		}
		System.out.println("--------");
	long  i = 11;
	while (i < 101 ) {
		System.out.println(i);
		i= i + 2; 
	}

	}

}
